         c1efield_Hmetricdqdq_c0_bks(2,iband,iefipert,iq1grad,istrpert)=half*doti

       end do !iefipert

     end do !iband

     !Clean the rf_hamiltonian
     call destroy_rf_hamiltonian(rf_hamkq)

     !Deallocations
     ABI_DEALLOCATE(kpg_k)
