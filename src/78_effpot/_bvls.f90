subroutine rotg ( sa, sb, c, s )

!*****************************************************************************80
!
!! ROTG defines a Givens rotation.
!
!  Discussion:
!
!    Given values A and B, this routine computes
!
!    SIGMA = sign ( A ) if abs ( A ) >  abs ( B )
!          = sign ( B ) if abs ( A ) <= abs ( B );
!
!    R     = SIGMA * ( A * A + B * B );
!
!    C = A / R if R is not 0
!      = 1     if R is 0;
!
!    S = B / R if R is not 0,
!        0     if R is 0.
!
!    The computed numbers then satisfy the equation
!
!    (  C  S ) ( A ) = ( R )
!    ( -S  C ) ( B ) = ( 0 )
!
!    The routine also computes
!
!    Z = S     if abs ( A ) > abs ( B ),
!      = 1 / C if abs ( A ) <= abs ( B ) and C is not 0,
!      = 1     if C is 0.
!
!    The single value Z encodes C and S, and hence the rotation:
!
!    If Z = 1, set C = 0 and S = 1;
!    If abs ( Z ) < 1, set C = sqrt ( 1 - Z * Z ) and S = Z;
!    if abs ( Z ) > 1, set C = 1/ Z and S = sqrt ( 1 - C * C );
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    15 June 2014
!
!  Author:
!
!    Original FORTRAN90 version by Charles Lawson, Richard Hanson.
!    This FORTRAN90 version by John Burkardt.
!
!  Reference:
!
!    Charles Lawson, Richard Hanson,
!    Solving Least Squares Problems,
!    SIAM, 1995,
!    ISBN: 0898713560,
!    LC: QA275.L38.
!
!  Parameters:
!
!    Input/output, real ( kind = 8 ) SA, the value of A.
!    On output, SA is overwritten with R.
!
!    Input, real ( kind = 8 ) SB, the value of B.
!
!    Output, real ( kind = 8 ) C, S, the cosine and sine of the
!    Givens rotation.
!
  implicit none

  real ( kind = 8 ) c
  real ( kind = 8 ) r
  real ( kind = 8 ) roe
  real ( kind = 8 ) s
  real ( kind = 8 ) sa
  real ( kind = 8 ) sb
  real ( kind = 8 ) scale

  roe = sb

  if ( abs ( sb ) < abs ( sa ) ) then
    roe = sa
  end if

  scale = abs ( sa ) + abs ( sb )

  if ( scale <= 0.0D+00 ) then
    c = 1.0D+00
    s = 0.0D+00
    return
  end if

  r = scale * sqrt ( ( sa / scale ) ** 2 + ( sb / scale ) ** 2 )

  if ( roe < 0.0D+00 ) then
    r = - r
  end if

  c = sa / r
  s = sb / r
  sa = r

  return
end
subroutine select_another_coeff ( m, n, iz, iz1, iz2, j, npp1, nsetp, free, &
  up, b, bnd, index, x, a, w, find, z )

!*****************************************************************************80
!
!! SELECT_ANOTHER_COEFF searches set Z for a new coefficient to solve for.
!
!  Discussion:
!
!    1. Search through set z for a new coefficient to solve for.
!    First select a candidate that is either an unconstrained
!    coefficient or else a constrained coefficient that has room
!    to move in the direction consistent with the sign of its dual
!    vector component.  Components of the dual (negative gradient)
!    vector will be computed as needed.
!
!    2. For each candidate start the transformation to bring this
!    candidate into the triangle, and then do two tests:  Test size
!    of new diagonal value to avoid extreme ill-conditioning, and
!    the value of this new coefficient to be sure it moved in the
!    expected direction.
!
!    3. if some coefficient passes all these conditions, set FIND = true,
!    The index of the selected coefficient is J = INDEX(IZ).
!
!    4. if no coefficient is selected, set FIND = false.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    18 June 2014
!
!  Author:
!
!    Original FORTRAN90 version by Charles Lawson, Richard Hanson.
!    This FORTRAN90 version by John Burkardt.
!
!  Reference:
!
!    Charles Lawson, Richard Hanson,
!    Solving Least Squares Problems,
!    SIAM, 1995,
!    ISBN: 0898713560,
!    LC: QA275.L38.
!
!  Parameters:
!
!    Input, integer ( kind = 4 ) M, the number of rows of A.
!
!    Input, integer ( kind = 4 ) N, the number of columns of A.
!
!    Output, integer ( kind = 4 ) IZ.
!
!    Input, integer ( kind = 4 ) IZ1, IZ2.
!
!    Input, integer ( kind = 4 ) J.
!
!    Input, integer ( kind = 4 ) NPP1.
!
!    Input, integer ( kind = 4 ) NSETP.
!
!    Input, logical ( kind = 4 ) FREE.
!
!    Input, real ( kind = 8 ) UP.
!
!    Input, real ( kind = 8 ) B(M).
!
!    Input, real ( kind = 8 ) BND(2,N).
!
!    Input, integer ( kind = 4 ) INDEX(N), defines the sets P, Z, and F.
!
!    Input, real ( kind = 8 ) X(N).
!
!    Input/output, real ( kind = 8 ) A(M,N), the matrix.
!
!    Input/output, real ( kind = 8 ) W(N).
!
!    Output, logical ( kind = 4 ) FIND.
!
!    Output, real ( kind = 8 ) Z(M).
!
  implicit none

  integer ( kind = 4 ) m
  integer ( kind = 4 ) n 

  real ( kind = 8 ) a(m,n)
  real ( kind = 8 ) b(m)
  real ( kind = 8 ) bnd(2,n)
  logical ( kind = 4 ) find
  logical ( kind = 4 ) free
  logical ( kind = 4 ) free1
  logical ( kind = 4 ) free2
  integer ( kind = 4 ) index(n)
  integer ( kind = 4 ) iz
  integer ( kind = 4 ) iz_index
  integer ( kind = 4 ) iz1
  integer ( kind = 4 ) iz2
  integer ( kind = 4 ) j
  integer ( kind = 4 ) npp1
  integer ( kind = 4 ) nsetp
  real ( kind = 8 ) up
  real ( kind = 8 ) w(n)
  real ( kind = 8 ) x(n)
  real ( kind = 8 ) z(m)

  find = .false.

  do iz_index = iz1, iz2

    iz = iz_index

    j = index(iz)
!
!  FREE1 = true if X(J) is not at the left end-point of its constraint region.
!  FREE2 = true if X(J) is not at the right end-point of its constraint region.
!  FREE = true if X(J) is not at either end-point of its constraint region.
!
    free1 = ( bnd(1,j) < x(j) )
    free2 = ( x(j) < bnd(2,j) )
    free = ( free1 .and. free2 )

    if ( free ) then

      call test_coef_j ( m, n, j, npp1, nsetp, free, up, b, x, a, w, &
        find, z )
!
!  Compute dual coefficient W(J).
!
    else

      w(j) = dot_product ( a(npp1:m,j), b(npp1:m) )
!
!  Can X(J) move in the direction indicated by the sign of W(J)?
!
      if ( w(j) < 0.0D+00 ) then
        if ( free1 ) then
          call test_coef_j ( m, n, j, npp1, nsetp, free, up, b, x, a, &
            w, find, z )
        end if
      else if ( 0.0D+00 < w(j) ) then
        if ( free2 ) then
          call test_coef_j ( m, n, j, npp1, nsetp, free, up, b, x, a, &
            w, find, z )
        end if
      end if

    end if

    if ( find ) then
      return
    end if

  end do

  return
end
subroutine termination ( ierr, m, n, npp1, b, w, rnorm )

!*****************************************************************************80
!
!! TERMINATION handles the termination of the BVLS algorithm.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    11 June 2014
!
!  Author:
!
!    Original FORTRAN90 version by Charles Lawson, Richard Hanson.
!    This FORTRAN90 version by John Burkardt.
!
!  Reference:
!
!    Charles Lawson, Richard Hanson,
!    Solving Least Squares Problems,
!    SIAM, 1995,
!    ISBN: 0898713560,
!    LC: QA275.L38.
!
!  Parameters:
!
!    Input, integer ( kind = 4 ) IERR, error flag.
!
!    Input, integer ( kind = 4 ) M.
!
!    Input, integer ( kind = 4 ) N.
!
!    Input, integer ( kind = 4 ) NPP1.
!
!    Input, real ( kind = 8 ) B(M).
!
!    Output, real ( kind = 8 ) W(N).
!
!    Output, real ( kind = 8 ) RNORM.
!
  implicit none

  integer ( kind = 4 ) m
  integer ( kind = 4 ) n

  real ( kind = 8 ) b(m)
  integer ( kind = 4 ) ierr
  integer ( kind = 4 ) npp1
  real ( kind = 8 ) nrm2
  real ( kind = 8 ) rnorm
  real ( kind = 8 ) w(n)
!
