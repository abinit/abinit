!
  rnorm = 0.0D+00

  if ( ierr <= 0 ) then

    if ( npp1 <= m ) then
      rnorm = nrm2 ( m + 1 - npp1, b(npp1:m) )
    else
      w(1:n) = 0.0D+00
    end if

  end if

  return
end
subroutine test_coef_j ( m, n, j, npp1, nsetp, free, up, b, x, a, w, &
  find, z )

!*****************************************************************************80
!
!! TEST_COEF_J
!
!  Discussion:
!
!    The sign of W(J) is OK for J to be moved to set P.
!
!    Begin the transformation and check new diagonal element to avoid
!    near linear dependence.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    11 June 2014
!
!  Author:
!
!    Original FORTRAN90 version by Charles Lawson, Richard Hanson.
!    This FORTRAN90 version by John Burkardt.
!
!  Reference:
!
!    Charles Lawson, Richard Hanson,
!    Solving Least Squares Problems,
!    SIAM, 1995,
!    ISBN: 0898713560,
!    LC: QA275.L38.
!
!  Parameters:
!
!    Input, integer ( kind = 4 ) M, the number of rows of A.
!
!    Input, integer ( kind = 4 ) N, the number of columns of A.
!
!    Input, integer ( kind = 4 ) J.
!
!    Input, integer ( kind = 4 ) NPP1.
!
!    Input, integer ( kind = 4 ) NSETP.
!
!    Input, logical ( kind = 4 ) FREE.
!
!    Input, real ( kind = 8 ) UP.
!
!    Input, real ( kind = 8 ) B(M).
!
!    Input, real ( kind = 8 ) X(N).
!
!    Input/output, real ( kind = 8 ) A(M,N), the matrix.
!
!    Input/output, real ( kind = 8 ) W(N).
!
!    Output, logical ( kind = 4 ) FIND.
!
!    Output, real ( kind = 8 ) Z(M).
!
  implicit none

  integer ( kind = 4 ) m
  integer ( kind = 4 ) n 

  real ( kind = 8 ) a(m,n)
  real ( kind = 8 ) asave
  real ( kind = 8 ) b(m)
  real ( kind = 8 ), parameter :: eps = 2.220446049250313D-016
  logical ( kind = 4 ) find
  logical ( kind = 4 ) free
  integer ( kind = 4 ) j
  real ( kind = 8 ) norm
  integer ( kind = 4 ) npp1
  real ( kind = 8 ) nrm2
  integer ( kind = 4 ) nsetp
  real ( kind = 8 ) sm
  real ( kind = 8 ) unorm
  real ( kind = 8 ) up
  real ( kind = 8 ) w(n)
  real ( kind = 8 ) x(n)
  real ( kind = 8 ) z(m)
  real ( kind = 8 ) ztest

  asave = a(npp1,j)

  call htc ( npp1, m, a(1:m,j), up )

  unorm = nrm2 ( nsetp, a(1:nsetp,j) )

  if ( eps * unorm < abs ( a(npp1,j) ) ) then
!
!  Column J is sufficiently independent.  Copy B into Z, update Z.
!
    z(1:m) = b(1:m)
!
!  Compute product of transformation and updated right-hand side.
!
    norm = a(npp1,j)
    a(npp1,j) = up

    if ( 0.0D+00 < abs ( norm ) ) then
      sm = dot_product ( a(npp1:m,j) / norm, z(npp1:m) ) / up
      z(npp1:m) = z(npp1:m) + sm * a(npp1:m,j)
      a(npp1,j) = norm
    end if

    if ( 0.0D+00 < abs ( x(j) ) ) then
      z(1:npp1) = z(1:npp1) + a(1:npp1,j) * x(j)
    end if
!
!  Adjust Z as though X(J) had been reset to zero.
!
    if ( free ) then

      find = .true.

    else
!
!  Solve for ZTEST, the proposed new value for X(J).
!  FIND indicates if ZTEST has moved away from X(J) in
!  the expected direction indicated by the sign of W(J).
!
      ztest = z(npp1) / a(npp1,j)
      find = ( w(j) < 0.0D+00 .and. ztest < x(j) ) .or. &
        ( 0.0D+00 < w(j) .and. x(j) < ztest )

    end if

  end if
!
!  If J was not accepted to be moved from set Z to set P,
!  restore A(NNP1,J).  Failing these tests may mean the computed
!  sign of W(J) is suspect, so here we set W(J) = 0.  This will
!  not affect subsequent computation, but cleans up the W() array.
!
  if ( .not. find ) then
    a(npp1,j) = asave
    w(j) = 0.0D+00
  end if

  return
end
