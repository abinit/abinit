  implicit none

  integer ( kind = 4 ) m
  integer ( kind = 4 ) n

  real ( kind = 8 ) a(m,n)
  real ( kind = 8 ) alpha
  real ( kind = 8 ) b(m)
  real ( kind = 8 ) bnd(2,n)
  logical ( kind = 4 ) hitbnd
  integer ( kind = 4 ) i
  integer ( kind = 4 ) ibound
  integer ( kind = 4 ) ierr
  integer ( kind = 4 ) ii
  integer ( kind = 4 ) index(n)
  integer ( kind = 4 ) ip
  integer ( kind = 4 ) iter
  integer ( kind = 4 ) itmax
  integer ( kind = 4 ) iz1
  integer ( kind = 4 ) jj
  integer ( kind = 4 ) jjj
  integer ( kind = 4 ) l
  integer ( kind = 4 ) npp1
  integer ( kind = 4 ) nsetp
  real ( kind = 8 ) x(n)
  real ( kind = 8 ) z(m)

  loopb: do
!
!  The solution obtained by solving the current set P is in the array Z().
!
    iter = iter + 1
    if ( itmax < iter ) then
      ierr = 4
      exit loopb
    end if
!
!  Set HITBND.
!  If HITBND is true, also set ALPHA, JJ, and IBOUND.
!
    call constrained_feasible ( nsetp, index, bnd, x, z, hitbnd, alpha, &
      jj, ibound )

    if ( .not. hitbnd ) then
      exit loopb
    end if
!
!  ALPHA will be between 0 and 1 for interpolation
!  between the old X and the new Z.
!
    do ip = 1, nsetp
      l = index(ip)
      x(l) = x(l) + alpha * ( z(ip) - x(l) )
    end do

    i = index(jj)
!
!  The exit test is done at the end of the loop, so the loop
!  will always be executed at least once.
!
    do
!
!  Modify A, B, and the index arrays to move coefficient I
!  from set P to set Z.
!
      call move_coef_i ( m, n, i, ibound, bnd, jj, b, a, x, nsetp, index, &
        npp1, iz1 )

      if ( nsetp <= 0 ) then
        exit loopb
      end if
!
!  See if the remaining coefficients in set P are feasible.  They should
!  be because of the way ALPHA was determined.  If any are infeasible
!  it is due to round-off error.  Any that are infeasible or on a boundary
!  will be set to the boundary value and moved from set P to set Z.
!
      ibound = 0
      jj = nsetp

      do jjj = 1, nsetp

        i = index(jjj)

        if ( x(i) <= bnd(1,i) ) then
          ibound = 1
          jj = jjj
          exit
        else if ( bnd(2,i) <= x(i) ) then
          ibound = 2
          jj = jjj
          exit
        end if

      end do

      if ( ibound <= 0 ) then
        exit
      end if

    end do
!
!  Copy B into Z.
!
    z(1:m) = b(1:m)
!
!  Solve and loop back again.
!
    do i = nsetp, 1, -1
      if ( i /= nsetp ) then
        z(1:i) = z(1:i) - a(1:i,ii) * z(i+1)
      end if
      ii = index(i)
      z(i) = z(i) / a(i,ii)
    end do

  end do loopb

  do ip = 1, nsetp
    i = index(ip)
    x(i) = z(ip)
  end do

  return
end
subroutine timestamp ( )

!*****************************************************************************80
!
!! TIMESTAMP prints the current YMDHMS date as a time stamp.
!
!  Example:
!
!    31 May 2001   9:45:54.872 AM
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    18 May 2013
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    None
!
  implicit none

  character ( len = 8 ) ampm
  integer ( kind = 4 ) d
  integer ( kind = 4 ) h
  integer ( kind = 4 ) m
  integer ( kind = 4 ) mm
  character ( len = 9 ), parameter, dimension(12) :: month = (/ &
    'January  ', 'February ', 'March    ', 'April    ', &
    'May      ', 'June     ', 'July     ', 'August   ', &
    'September', 'October  ', 'November ', 'December ' /)
  integer ( kind = 4 ) n
  integer ( kind = 4 ) s
  integer ( kind = 4 ) values(8)
  integer ( kind = 4 ) y

  call date_and_time ( values = values )

  y = values(1)
  m = values(2)
  d = values(3)
  h = values(5)
  n = values(6)
  s = values(7)
  mm = values(8)

  if ( h < 12 ) then
    ampm = 'AM'
  else if ( h == 12 ) then
    if ( n == 0 .and. s == 0 ) then
      ampm = 'Noon'
    else
      ampm = 'PM'
    end if
  else
    h = h - 12
    if ( h < 12 ) then
      ampm = 'PM'
    else if ( h == 12 ) then
      if ( n == 0 .and. s == 0 ) then
        ampm = 'Midnight'
      else
        ampm = 'AM'
      end if
    end if
  end if

  write ( *, '(i2.2,1x,a,1x,i4,2x,i2,a1,i2.2,a1,i2.2,a1,i3.3,1x,a)' ) &
    d, trim ( month(m) ), y, h, ':', n, ':', s, '.', mm, trim ( ampm )

  return
end
